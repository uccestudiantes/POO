<b>EL SERVICIO Y LA INFORMACIÓN</b>
<p align="justify">El servicio que ofrece el Portal del BECAS ALIANZA tiene por finalidad brindar al 'usuario' información relacionada con los servicios que presta este Instituto. La prestación del servicio del Portal por parte del BECAS ALIANZA es de carácter libre y gratuito para los 'usuarios'; no obstante, la utilización de algunos servicios sólo puede hacerse como 'usuario registrado' sin que esto signifique incurrir en un costo monetario.
<br>
<b>REGISTRO</b>
Efectuar la labor de registro no es necesario en todos los casos, sin embargo, sí es una acción imprescindible al utilizar el servicio de impresión del recibo de pago, al querer recibir atención personalizada por el Centro de Atención Virtual, al querer hacer uso del servicio de chat, pago en línea, algunas consultas del estado de cuenta y otros según el caso.
<br>
Sin perjuicio de ello, en caso de que el 'usuario' elija o deba registrarse para utilizar algún servicio deberá completar el formulario con su información personal de manera exacta y precisa en la sección regístrese en cada una de estas secciones del Portal Web.
<br>
Por su parte, salvo en los campos en los que se indique lo contrario, las respuestas a las preguntas sobre 'Registro de usuarios', son voluntarias, sin que la falta de contestación implique una disminución en la calidad o cantidad de los servicios correspondientes, a menos de que se indique otra cosa.
<br>
La información personal como 'usuario registrado' está protegida con una clave que sólo el 'usuario' conoce y con la que podrá tener acceso al servicio que la requiera las veces que necesite acceder a él. El 'usuario' podrá modificar su información como 'usuario registrado' utilizando su nombre de 'usuario' y su clave.
<br>
El 'usuario registrado' se comprometerá a notificar al BECAS ALIANZA en forma inmediata y a través de un medio fehaciente, cualquier uso no autorizado de su clave.
<br>
El Portal Web del BECAS ALIANZA, se reserva el derecho de rechazar cualquier solicitud de registro o de cancelar un registro previamente aceptado.
<br>
Asimismo el BECAS ALIANZA se reserva el derecho de enviar información que considere de interés para el 'usuario' a la dirección de correo que éste indicó en el formulario de registro. No obstante lo anterior, El BECAS ALIANZA reconoce y se adhiere a las iniciativas contra el correo no solicitado ("spamming"), entendiendo que la información enviada a sus usuarios no contiene publicidad comercial, e incorporando de forma clara y sencilla los pasos que el 'usuario' debe seguir en el caso de que no quiera recibir más la información que le envía el BECAS ALIANZA a su correo personal.
<br>
<b>MODIFICACIÓN DE CONTENIDOS</b>
El Portal del BECAS ALIANZA se reserva el derecho de modificar o de corregir el contenido de las condiciones y de este sitio web en cualquier momento y sin previo aviso a los usuarios.
<br>
Todos los derechos que no son expresamente concedidos en los términos de las presentes disposiciones reglamentarias están reservados por el Portal del BECAS ALIANZA, y por lo tanto, deben ser de obligatorio acatamiento. El usuario que contravenga lo reglamentado en este documento deberá sujetarse a las sanciones civiles y penales que establece la ley colombiana sobre derechos de autor en el ámbito nacional e internacional.
<br>
<b>PRIVACIDAD DE LA INFORMACIÓN</b>
La recolección y tratamiento automatizado de los datos personales tiene como finalidad el buen y fluido mantenimiento de la relación establecida entre el 'usuario' y el BECAS ALIANZA, representado en acciones como la gestión, administración, prestación, ampliación y mejora de los servicios en los que el 'usuario' decida suscribirse, la adecuación de dichos servicios a sus preferencias y gustos, el estudio de la utilización de los servicios por parte de los 'usuarios', el diseño de nuevos servicios relacionados con dichos servicios, el envío de actualizaciones de los servicios y, el envío por medios tradicionales y electrónicos de información técnica, operativa y comercial acerca de productos y servicios ofrecidos por el BECAS ALIANZA a través de su página web.
<br>
En los casos en que se requiera información de los particulares, se aclarará cuáles son los fines para los que ésta se necesita y además se brindará seguridad de que no será utilizada para propósitos diferentes a los informados en la petición; igualmente, no será divulgada a terceros sin consentimiento de quien suministre la información, salvo en los casos en que las leyes colombianas lo exijan.
<br>
<b>PROPIEDAD INTELECTUAL</b>
Se entenderá por 'contenidos' todas las informaciones, mensajes, gráficos, fotografías, dibujos, diseños, logotipos, nombres, marcas u otro tipo de archivos de audio o de imagen, cualquier software en general o clase de material accesible a través de los canales web del BECAS ALIANZA; en adelante se les denominará 'contenidos'.
<br>
Las obras protegidas por los derechos de autor que se encuentren dentro de la página principal del BECAS ALIANZA, hacen parte del patrimonio de la entidad pública y por lo tanto son considerados bienes fiscales, razón por la que su utilización debe estar expresamente autorizada y así mismo se debe informar claramente al ciudadano qué puede hacer y qué no con el material alojado.
<br>
Si una entidad pública desea hacer uso (reproducción, transformación, o puesta a disposición) de materiales y obras protegidas por el derecho de autor, debe cerciorarse de contar con la debida autorización del titular de los derechos sobre dicho material.
<br>
La protección de los derechos de autor se aplica sobre los textos, imágenes, dibujos, fotografías o cualquier otro tipo de material gráfico o visual, también aplica para los programas informáticos y las bases de datos. Cualquier material de este tipo que sea tomado de alguna fuente externa a la entidad debe ser considerado como una cita textual, por lo tanto debe ir entre comillas dobles ("") y se debe incluir la referencia del lugar donde fue tomada la cita, con al menos, los siguientes datos: autor(es), libro(s) o la fuente de la que fue tomada la información, al igual que la fecha de publicación.
<br>
La protección de los derechos de propiedad intelectual y propiedad industrial se observarán en lo relativo a marcas, nombres comerciales, logos, enseñas, lemas, nombres de dominio, secretos empresariales, saber-hacer, diseños industriales, patentes, modelos de utilidad y derecho de autor.
<br>
El desconocimiento de las normas vigentes en materia de derechos de autor, derechos de propiedad intelectual y de propiedad industrial puede generar acciones civiles o penales.
